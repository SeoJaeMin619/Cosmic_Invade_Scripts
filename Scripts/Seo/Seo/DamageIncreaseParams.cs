internal class DamageIncreaseParams
{
    private int damageIncreaseAmount;
    private float damageIncreaseDuration;

    public DamageIncreaseParams(int damageIncreaseAmount, float damageIncreaseDuration)
    {
        this.damageIncreaseAmount = damageIncreaseAmount;
        this.damageIncreaseDuration = damageIncreaseDuration;
    }
}